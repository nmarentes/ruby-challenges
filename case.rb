weather = 'sunny'

case weather
    
when 'rainy'
    puts "Get out that raincoat and those rainboots, you're gonna get wet!"
when 'snowy'
    puts "Honestly I don't know if you have the wardrobe for this. Layer up! You're gonna need a coat, scarf, boots, gloves, and a couple of layers!"
when 'foggy'
    puts "Hmmm, layer up, maybe bring a flashlight... "
when 'cloudy' 
    puts "Looks like sweater weather to me... maybe a hat too, just in case."
when 'sunny'
    puts "Ahhh, it's going to be really nice today! Time to pull out that day dress!"
end
